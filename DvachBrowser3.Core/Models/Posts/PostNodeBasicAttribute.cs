namespace DvachBrowser3.Posts
{
    /// <summary>
    /// Базовый атрибут.
    /// </summary>
    public enum PostNodeBasicAttribute
    {
        /// <summary>
        /// Наклонный текст.
        /// </summary>
        Italic,

        /// <summary>
        /// Жирный текст.
        /// </summary>
        Bold,

        /// <summary>
        /// Моноширинный текст.
        /// </summary>
        Monospace,

        /// <summary>
        /// Подчёркнутый текст.
        /// </summary>
        Underscore,

        /// <summary>
        /// Текст с чертой сверху.
        /// </summary>
        Overscore,

        /// <summary>
        /// Спойлер.
        /// </summary>
        Spoiler,

        /// <summary>
        /// Зачёркнутый текст.
        /// </summary>
        Strikeout,

        /// <summary>
        /// Сдвинутый вверх текст.
        /// </summary>
        Sup,

        /// <summary>
        /// Сдвинутый вниз текст.
        /// </summary>
        Sub,

        /// <summary>
        /// Цитата.
        /// </summary>
        Quote,

        /// <summary>
        /// Сообщение от модератора.
        /// </summary>
        Moderatorial,

        /// <summary>
        /// Нумерованный список.
        /// </summary>
        OrderedList,

        /// <summary>
        /// Ненумерованный список.
        /// </summary>
        UnorderedList,

        /// <summary>
        /// Параграф.
        /// </summary>
        Paragraph,
    }
}